# ── Grid helpers ─────────────────────────────────────────────────────────
def get_row(n):
    """Number 1-90 → row 0-5"""
    return (n-1) // 15

def get_line(n):
    """Number 1-90 → line (vertical col) 0-4
    Red lines every 3 numbers within each row:
    positions 1-3→L1, 4-6→L2, 7-9→L3, 10-12→L4, 13-15→L5 (within each row)
    """
    return ((n-1) % 15) // 3

