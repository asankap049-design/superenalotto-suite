    print(f"\n  Numbers per line (col) across {TOTAL} draws:")
    for i,t in enumerate(line_totals):
        pct = t/(TOTAL*6)*100
        bar = "█" * int(pct/2)
        print(f"    {LINE_NAMES[i]:5}: {t:4d} numbers ({pct:.1f}%) {bar}")

    # Most common row patterns (which row combinations appear most)
    print(f"\n  Most common 'active rows' combinations (top 10):")
    combo_cnt = Counter()
    for _, nums in DRAWS:
        rc, _ = draw_pattern(nums)
        active = tuple(i+1 for i,v in enumerate(rc) if v>0)
        combo_cnt[active] += 1
    for combo, cnt in combo_cnt.most_common(10):
        pct = cnt/TOTAL*100
        rows_str = "+".join(f"R{r}" for r in combo)
        print(f"    {rows_str:<25} {cnt:3d}x ({pct:.1f}%)")

    # Consecutive miss streaks
    print(f"\n  Current consecutive misses per row (last draw: {DRAWS[-1][0]}):")
    for r in range(6):
        streak = 0
        for _, nums in reversed(DRAWS):
            if not any(get_row(n)==r for n in nums):
                streak += 1
            else:
                break
        print(f"    {ROW_NAMES[r]:12}: {streak} consecutive draws without hit")

    print(f"\n  Current consecutive misses per line:")
    for l in range(5):
        streak = 0
        for _, nums in reversed(DRAWS):
            if not any(get_line(n)==l for n in nums):
                streak += 1
            else:
                break
        print(f"    {LINE_NAMES[l]}: {streak} consecutive draws without hit")

