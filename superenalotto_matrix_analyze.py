# ── Convert draw to row/line pattern ─────────────────────────────────────
def draw_pattern(nums):
    """Returns (row_counts, line_counts) — how many numbers in each row/line"""
    row_cnt = [0]*6
    line_cnt = [0]*5
    for n in nums:
        row_cnt[get_row(n)] += 1
        line_cnt[get_line(n)] += 1
    return row_cnt, line_cnt

# ── Print draw pattern ───────────────────────────────────────────────────
def print_draw(date, nums, prefix=""):
    row_cnt, line_cnt = draw_pattern(nums)
    row_str = " ".join(f"R{i+1}:{v}" for i,v in enumerate(row_cnt) if v>0)
    line_str = " ".join(f"L{i+1}:{v}" for i,v in enumerate(line_cnt) if v>0)
    print(f"{prefix}{date} | {sorted(nums)} | {row_str} | {line_str}")

